// Program to print given pattern

public class Pattern2
{
    public static void main()
    {
        for(int i = 1; i<=4; i++)
        {
            for(int j = 1; j<=i; j++)
            {
                System.out.print("1");
            }
            System.out.println();
        }
    }
}